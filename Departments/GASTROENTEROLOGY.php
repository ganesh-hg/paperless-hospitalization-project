  Diseases affecting the gastrointestinal tract, which include the organs from mouth into anus, along the alimentary canal, are the focus of this speciality. Physicians practicing in this field are called gastroenterologists. They have usually completed about eight years of pre-medical and medical education, a year-long internship , three years of an internal medicine residency, and three years in the gastroenterology fellowship. Gastroenterologists perform a number of diagnostic and therapeutic procedures including colonoscopy, esophagogastroduodenoscopy , endoscopic retrograde cholangiopancreatography, endoscopic ultrasound, and liver biopsy. Some gastroenterology trainees will complete a "fourth-year" although this is often their seventh year of graduate medical educatio in transplant hepatology, advanced interventional endoscopy, inflammatory bowel disease, motility, or other topics.<br>
  Advanced endoscopy, sometimes called interventional or surgical endoscopy, is a sub-specialty of gastroenterology that focuses on advanced endoscopic techniques for the treatment of pancreatic, hepatobiliary, and gastrointestinal disease. Interventional gastroenterologists typically undergo an additional year of rigorous training in advanced endoscopic techniques including endoscopic retrograde cholangiopancreatography, endoscopic ultrasound-guided diagnostic and interventional procedures, and advanced resection techniques including endoscopic mucosal resection and endoscopic submucosal dissection. Additionally, the performance of endoscopic bariatric procedures is also performed by some advanced endoscopists.</p>
